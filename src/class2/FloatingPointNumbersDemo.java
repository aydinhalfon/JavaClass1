package class2;

public class FloatingPointNumbersDemo {
    public static void main(String[] args) {
        /* by default all the floating point numbers are doubles,
        that's why we need to write an F or f at the end
        of the float type variables but we don't need to worry
        about it as 99% of the time we'll be using a double
         */
        float grocery=20.5F; // is a smaller size of data type
        System.out.println(grocery);
        double price=25.78; // is the bigger size can hold double the amount of float
        System.out.println(price);




    }






}
